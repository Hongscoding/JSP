package baen;

public class registerList {
	private String regNo; 
	private String regName;
	private String reglecName;
	private int reglecCode; 
	private int regMid;
	private int regFinal;
	private int regTotal;
	private String regGrade;
	
	public String getRegNo() {
		return regNo;
	}
	public void setRegNo(String regNo) {
		this.regNo = regNo;
	}
	public String getRegName() {
		return regName;
	}
	public void setRegName(String regName) {
		this.regName = regName;
	}
	public String getReglecName() {
		return reglecName;
	}
	public void setReglecName(String reglecName) {
		this.reglecName = reglecName;
	}
	public int getReglecCode() {
		return reglecCode;
	}
	public void setReglecCode(int reglecCode) {
		this.reglecCode = reglecCode;
	}
	public int getRegMid() {
		return regMid;
	}
	public void setRegMid(int regMid) {
		this.regMid = regMid;
	}
	public int getRegFinal() {
		return regFinal;
	}
	public void setRegFinal(int regFinal) {
		this.regFinal = regFinal;
	}
	public int getRegTotal() {
		return regTotal;
	}
	public void setRegTotal(int regTotal) {
		this.regTotal = regTotal;
	}
	public String getRegGrade() {
		return regGrade;
	}
	public void setRegGrade(String regGrade) {
		this.regGrade = regGrade;
	}
	
	
}
